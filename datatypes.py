# Conversion of types
# integer to string
de = str(input("Enter a str number:"))
print("Converted integer:", de)
print(type(de))

# string to integer
f = str(input("Enter a str number:"))
f = int(f)
print("Converted integer:", f)
print(type(f))

# Float to integer
g = float(input("Enter a float number:"))
g = int(g)
print("The converted number from float to int:", g)
print(type(g))
